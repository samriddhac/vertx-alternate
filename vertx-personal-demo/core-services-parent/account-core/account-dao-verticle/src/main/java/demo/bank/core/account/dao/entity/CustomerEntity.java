package demo.bank.core.account.dao.entity;

public class CustomerEntity {


    private Integer id;
 
    private String name;
    
    public CustomerEntity() {}
    
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
